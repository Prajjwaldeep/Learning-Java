class for_16
{
    public static void main(String args[])
    {
        int c,r,n=1;
        for (c=1; c<=4; c++)
        {
        for (r=c; r<=4; r++)
      {
        if (r%2==0)
        {
        System.out.print("*");
        n++;
    }
        else
        System.out.print("#");
    }
        System.out.println();
}
}
}
