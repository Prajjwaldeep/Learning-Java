class Trapezium{
public static void main(double a,double b,double c)
{
    double d=0.5*(a+b)*c;
    System.out.println("1st Parallel Side"+" "+"="+" "+a);
    System.out.println("2nd Parallel Side"+" "+"="+" "+b);
    System.out.println("Height"+" "+"="+" "+c);
    System.out.println("Area"+" "+"="+" "+d);
}
}